namespace BoardClassics.Games
{
  /// <summary>
  /// Represents the type of piece on a board
  /// </summary>
  public enum BoardPieceType
  {
    /// <summary>
    /// Represents an empty space
    /// </summary>
    None,

    /// <summary>
    /// Represents a piece belonging to the black player
    /// </summary>
    Black,

    /// <summary>
    /// Represents a piece belonging to the white player
    /// </summary>
    White
  }
}